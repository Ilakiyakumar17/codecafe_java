package ProgramPackage1;

public class CharDemo {

	public static void main(String[] args) {
		
		char ch = 'a';
		System.out.println(ch);
		 
		char ch1 = 65;
		System.out.println(ch1);
		
		char var1 = '\u00A7';
		System.out.println(var1);
		
		char var2 = '\u20AC';
		System.out.println(var2);
		
		int a = 'A';
		System.out.println(a);
		
	}

}
